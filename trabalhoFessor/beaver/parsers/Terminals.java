package parsers;

/**
 * This class lists terminals used by the
 * grammar specified in "lang.grammar".
 */
public class Terminals {
	static public final short EOF = 0;
	static public final short ID = 1;
	static public final short AP = 2;
	static public final short NUM = 3;
	static public final short MULT = 4;
	static public final short PLUS = 5;
	static public final short LB = 6;
	static public final short RB = 7;
	static public final short IF = 8;
	static public final short COLON = 9;
	static public final short EQ = 10;
	static public final short FP = 11;
	static public final short SEMI = 12;
}
